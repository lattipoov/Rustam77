"""
Нарисуйте ёлку (вид ёлки произвольный, на картинке только пример)
 высоту ёлки должен задавать пользователь
"""
n = int(input('Введите высоту елки: '))
for i in range(n):
    k1 = n - i - 1
    k2 = n + i - 1
    for j in range(k2 + 1):
        if j >= k1:
            print('*', end='')
        else:
            print(' ', end='')
    print()


