"""
Вы пользуетесь общественным транспортом? Вероятно, вы расплачивались за проезд и
получали билет с номером. Счастливым билетом называют такой билет с шестизначным номером,
где сумма первых трех цифр равна сумме последних трех. Т.е. билет с номером 385916 – счастливый,
т.к. 3+8+5=9+1+6. Вам требуется написать программу, которая выводит на экран все доступные
счастливые билеты с подсчетом их количество
"""
number = 0
for n in range(0, 1000000):
    n1 = n // 100000
    n2 = (n % 100000) // 10000
    n3 = (n % 10000) //  1000
    n4 = (n % 1000) // 100
    n5 = (n % 100) // 10
    n6 = n % 10
    if n1 + n2 + n3 == n4 + n5 + n6:
        number += 1
        print(f'Счастливый билет №{number}: {n} ')
percent = number / 999999 * 100
rpercent = round(percent, 2)
print(f'Всего {rpercent}')


