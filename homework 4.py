"""
Написать программу «Банк». Вводится сумма вклада и количество лет.
Рассчитать сколько денег будет на счету пользователя к концу времени вклада.
Решить задачу по системе «сложный процент» (процент рассчитывается от новой суммы)
Годовой процент считать равным 10%.
"""
deposit = int(input('Введите сумму  влада: '))
years = int(input('Введите количество лет для вклада: '))
year_pass = 0
for i in range(1, years + 1):
    year_pass += 1
    deposit += ((deposit * 10)//100)
    print(f'{year_pass} лет ваш вклад составит: {deposit}')
print(f'Через {years} лет ващ вклад составит: {deposit}')


