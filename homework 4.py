#lesson 4,exersise 4.
"""
Многие спортсмены жаловались, что судья слишком тихо отсчитывает секунды,
оставшиеся до старта («Три!.. Два!.. Один!..»). Фирма Go Ahead купила табло для
наглядного отображения оставшегося времени.
"""
import  datetime
from time import  sleep
data = int(input("Укажите время для таймера (в минутах): "))
timer = datetime.timedelta(minutes=data)
for i in range(data*60):
      s = datetime.timedelta(seconds=1)
      timer -= s
      print(f'{timer}', end='')
      sleep(1)


